let variable = "Je suis une chaine.";
console.log(typeof variable); // typeof pour retourner le type de la variable.
variable = 42;
console.log(typeof variable);

// Number
let age = 25; // number
let poids = 75.5; // number
console.log(typeof poids);

// Booléen : Vrai ou faux
let estMajeur = true;

// undefined : variable non initialisée
let inconnu;
console.log(typeof inconnu);

// Null : absence de valeur
let vide = null;

// Conversion de types (Coercition)
let somme = "5" + 2;
console.log(typeof somme);

let produit = "5" * 2;
console.log(typeof produit)

